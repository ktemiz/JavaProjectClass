package HomeWorks;

public class ArraysExamples {
	
	public static void main(String[] args) {
		
		char[] word = {'s','a','y','b','n','c','t','d','a','e','x'};
		
		int size=word.length;
		
	for(int i = 0; i<=size-1; i++) {
			
		switch (word[i]) {
		
		case 's':
			System.out.print(word[i]);
			break;
		case 'y':
			System.out.print(word[i]);
			break;
		case 'n':
			System.out.print(word[i]);
			break;
		case 't':
			System.out.print(word[i]);
			break;
		case 'a':
			System.out.print(word[i]);
			break;
		case 'x':
			System.out.print(word[i]);
			break;
		}
			  
	}
	}
}
